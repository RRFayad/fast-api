like_coffee = True
likes_tea = False

favorite_food = 'Pizza'
favorite_number = 32

"""
Boolean operations and comparison operators in Python
"""

# Equality operator
print(favorite_food == 'Pizza')
# Inequality operator
print(favorite_number != 10)
# Greater than operator
print(favorite_number > 20)
# Less than operator
print(favorite_number < 50)
# Greater than or equal to operator
print(favorite_number >= 32)
print(favorite_number <= 40)

""" 
Logical operators
"""

# AND operator
print(like_coffee and likes_tea)
# OR operator
print(like_coffee or likes_tea)
# NOT operator
print(not likes_tea)

name = "Alice"

print("ALICE TEST:", 1 == 1 and name == 'Alice')
